package com.bptn.course._00_java_practice._04_flow_control._01_while_loop;

public class CodingExercise {

	public static void main(String[] args) {
		//declare and instantiate a variable
		int i = 2;
		
		//create a while loop with a condition that can be falsified to stop the loop if the condition is not met
		
		while(i<=20) {
			System.out.println(i);
			
			//increment the value of the variable to continue the loop
			i++;
		}

	}

}
