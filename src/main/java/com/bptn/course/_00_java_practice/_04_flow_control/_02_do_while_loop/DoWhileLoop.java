package com.bptn.course._00_java_practice._04_flow_control._02_do_while_loop;

public class DoWhileLoop {

	public static void main(String[] args) {
		//declare and instantiate a variable
		
		int i = 0;
		
		//create a do loop
		do  {
			System.out.println(i);
			
			//increment the value to continue the loop
			i++;
		}
		
		//create a while condition that can be falsified and stop the loop
		while (i<=10);

	}

}
