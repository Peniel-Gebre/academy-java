package com.bptn.course._03_flow_control._01_while_loop;

public class WhileLoop {

	    public static void main(String[] args) {
	        int i = 0;
	        // The above integer i will be used to run this code here:
	        while (i < 10){
	            System.out.println(i);
	            i = i + 2;
	        }
	    }
	}
